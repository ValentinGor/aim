                            <p>
                                If you are having an urgent need to prepare transfer pricing documentation, you have to be ready for a time-consuming process. The amount and
                                complexity of work associated with it can easily turn this task into a real <span>pain in the neck.</span>
                            </p>
                            <p>
                                However, first you have to figure out whether you actually fall within the scope of the Article 39 or Paragraph 140.5.4 of the of the Tax Code
                                of
                                Ukraine and whether transfer pricing is something you have to deal with in the first place.
                            </p>
                            <h2>
                                You are expected to submit transfer pricing documentation, if your transactions are recognized as controlled.
                            </h2>
                            <p>
                                The very definition of controlled transactions has its hidden pitfalls, which is why it is hard to tackle this matter right away, especially
                                against the backdrop of the ever-changing terms of the tax legislation.
                            </p>
                            <p>
                                In particular, in 2017-2018, changes were made both to the threshold amount of transactions involving counterparty and the requirements as to
                                the
                                taxpayer’s minimum net profit. Aside from that, some of the criteria for recognizing transactions as controlled were extended.
                            </p>
                            <img class="img-fluid" src="img/img_1.png" alt="img">
                            <h3>
                                Let's start off with the latter and try figure out when transactions are not deemed controlled.
                            </h3>
                            <p>
                                It stands to mention that the sharp eye of the State Fiscal <span>Service</span> has zero interest in "small" transactions: for the transaction
                                to
                                be deemed
                                controlled, the <strong>sum of transactions involving one counterparty</strong> (by direct agreements or through intermediaries that perform no
                                major functions
                                within the framework of the agreement) shall start from <strong>UAH 10 million</strong> in 2017-2018 (or UAH 5 million in 2015-2016).
                            </p>
                            <p>
                                An additional criterion for recognizing transactions as controlled is the total amount of taxpayer's income resulting from all types of
                                activities.
                                In 2015-2016, the threshold amount was <strong>UAH 50 million</strong> and has been increased to UAH
                            </p>
                            <h3 class="lit-h">
                                Companies having smaller revenues cannot have controlled transactions.
                            </h3>
                            <p>
                                <strong>The counterparty</strong> is another crucial aspect when it comes to controlled transaction. Below is a list of criteria, according to
                                which the transaction
                                is recognized as controlled in 2018, obviously in case of the relevant sum of transactions and total amount of taxpayer’s income.
                            </p>
                            <ul>
                                <li><span>Counterparty is an affiliated person</span></li>
                                <li><span>Counterparty is a commission agent</span></li>
                                <li><span>Counterparty is located in the country with low profit tax</span></li>
                                <li><span>Counterparty’s legal form of organization exempts it from profit tax</span></li>
                            </ul>

                            <p>
                                <span class="imitation-h3">Please note! </span> The list of given criteria is for the year 2018. The terms and conditions with respect to the
                                previous years may differ.
                            </p>
                            <h2>
                                Transfer pricing: the price of the violation
                            </h2>
                            <img class="img-fluid img-inf" src="img/img_2.png" alt="img">
                            <h4>
                                So, let’s sum up.
                            </h4>
                            <p>
                                Your transaction is not deemed controlled, if at least one of the following criteria is met:
                            </p>
                            <ul class="point-circle-ul">
                                <li class="point-circle"><span>The total amount of taxpayer's income for the year does not exceed UAH 150 million; </span></li>
                                <li class="point-circle"><span>Volume of transaction involving a non-resident does not exceed UAH 10 million;</span></li>
                                <li class="point-circle"><span>The counterparty does not meet any of the options given in the table above.</span></li>
                            </ul>
                            <h5>
                                However, even if your transaction is not controlled, it does not mean that you will avoid having to prepare transfer pricing documentation
                                altogether.
                            </h5>
                            <p>
                                Pursuant to Paragraph 140.5.4 of the Tax Code of Ukraine, you are required to submit documentation regarding transactions which are not
                                recognized
                                as controlled, should they involve counterparties:
                            </p>
                            <ul>
                                <li class="point-circle">
                                    <span>based in the countries with low profit tax (from the relevant list approved by the Cabinet of Ministers of Ukraine);</span></li>
                                <li class="point-circle"><span>that have legal form of organization which implies payment of profit tax in a country where the counterparty is a resident</span>
                                </li>
                                <li class="point-circle">
                                    <span>the counterparty is a non-profit organization or state-financed organization and is going to provide goods / <span>services</span> worth UAH 93,075 UAH and more in the course of 2018.</span>
                                </li>
                            </ul>
                            <p>
                                <strong>In all such instances, adjusting controlled transaction’s price</strong> by the taxpayer and increasing the income up to a figure of
                                30% of
                                the invoiced
                                value of the transaction can serve as an alternative to documentation preparation. Traditionally, the revenues should be increased only with
                                respect to very small transactions (those amounting up to UAH 150-200 thousand). Should transactions involving the counterparty exceed this
                                amount,
                                it would be cheaper to place an order for <strong>preparation of transfer pricing documentation.</strong>
                            </p>
